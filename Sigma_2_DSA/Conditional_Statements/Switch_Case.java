package Conditional_Statements;

public class Switch_Case {
    public static void main(String[] args) {
        char ch = 'b';
        switch (ch) {
            case 'd' :
                System.out.println("samosa");
                break;
            case 'c':
                System.out.println("burger");
                break;
            case 'b': 
                System.out.println("mango shake");
                break;
            default:
                System.out.println("We woke up");
        }
    }
}
